    train_set.shuffle()
    if data_args.dataset_training_split == "all":
        raw_l = len(train_set)
        l = len(train_set)
        train_set = train_set[:l]
    elif data_args.dataset_training_split == "282":
        raw_l = len(train_set)
        l = len(train_set)
        l = int(l*0.2)
        train_set = train_set[:l]
    elif data_args.dataset_training_split == "288":
        raw_l = len(train_set)
        l = len(train_set)
        l = int(l*0.2)
        train_set = train_set[l:]
    elif data_args.dataset_training_split == "55-":
        raw_l = len(train_set)
        l = len(train_set)
        l = int(l*0.5)
        train_set = train_set[:l]
    elif data_args.dataset_training_split == "55+":
        raw_l = len(train_set)
        l = len(train_set)
        l = int(l*0.5)
